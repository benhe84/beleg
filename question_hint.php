<h2>Hinweise zur Fragenerstellung</h2>
<h3>Es gibt 3 Fragetypen.</h3>
<ul>
	<li>Freie Fragen (FF)</li>
	<li>Multiple-Choice Einfachwahlfragen (MC1)</li>
	<li>Multiple-Choice Mehrfachwahlfragenfragen (MC2+)</li>
</ul>
<p>Bei allen Fragen, wird der eingegebene Hinweis beim 2. L&ouml;sungsversuch gezeigt</p>
<hr />
<h4>Freie Fragen</h4>
<p>Bei freien Fragen muss in das Feld Antwort 1 ein Regul&auml;rer PHP Ausdruck geschrieben werden. Dieser kann auf der folgenden <a href="https://regex101.com/" target="_blank">Website</a> erzeugt und getestet werden</p>
<hr />
<h4>Multiple-Choice Einfachwahlfragen</h4>
<p>In in das Feld Antwort 1 die Richtige L&ouml;sung einzutrag. Die anderen Felder mit falschen Antworten f&uuml;llen.</p>
<hr />
<h4>Multiple-Choice Mehrfachwahlfragen</h4>
<p>Je nach Auswahl sind die Antworten 1-Gew&auml;hlt die richtigen. Die anderen Felder mit falschen Antworten f&uuml;llen.<br />
Es wird nur Richtig ausgewertet, wenn nur alle richtigen Antworten gew&auml;hlt sind.</p>